#deklarasi variabel
nilai_x = int(4)

#menampilkan nilai dan tipe data nilai X
print('nilai x adalah : ', nilai_x)
print('tipe data x adalah : ', type(nilai_x))

#konversi tipe data nilai X ke float
nilai_konversi = float(nilai_x)

#menampilkan nilai dan tipe data hasil konversi
print('nilai x setelah konversi adalah : ', nilai_konversi)
print('tipe data x setelah konversi adalah : ', type(nilai_konversi))